    private void jButton1ActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jButton1ActionPerformed
        // TODO add your handling code here:
         int bookingIndex = jComboBox1.getSelectedIndex();
        if (bookingIndex == -1) {
            JOptionPane.showMessageDialog(this, "Please select a booking!");
            return;
        }
        if (jTextField1.getText().isEmpty()) {
            JOptionPane.showMessageDialog(this, "Please enter the amount!");
            return;
        }

        try {
            Payment p = new Payment();
            p.setBookingId(bookingList.get(bookingIndex).getId());
            p.setAmount(Double.parseDouble(jTextField1.getText()));
            p.setPaymentDate(new java.sql.Date(System.currentTimeMillis()));
            p.setMethod(jComboBox2.getSelectedItem().toString());
            p.setStatus("Paid");

            if (paymentDAO.addPayment(p)) {
                JOptionPane.showMessageDialog(this, "Payment added successfully!");
                loadPayments();
                clearFields();
            } else {
                JOptionPane.showMessageDialog(this, "Failed to add payment!");
            }
        } catch (NumberFormatException e) {
            JOptionPane.showMessageDialog(this, "Amount must be a number!");
        }
    }//GEN-LAST:event_jButton1ActionPerformed

    private void jButton2ActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jButton2ActionPerformed
        // TODO add your handling code here:
         if (selectedId == -1) {
            JOptionPane.showMessageDialog(this, "Please select a payment from the table!");
            return;
        }
        int confirm = JOptionPane.showConfirmDialog(this, "Are you sure you want to delete this payment?");
        if (confirm == 0) {
            if (paymentDAO.deletePayment(selectedId)) {
                JOptionPane.showMessageDialog(this, "Payment deleted successfully!");
                loadPayments();
                clearFields();
            } else {
                JOptionPane.showMessageDialog(this, "Failed to delete payment!");
            }
        }
    }//GEN-LAST:event_jButton2ActionPerformed

