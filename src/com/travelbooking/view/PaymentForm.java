    private void loadBookings() {
        bookingList = bookingDAO.getAllBookings();
        jComboBox1.removeAllItems();
        for (Booking b : bookingList) {
            jComboBox1.addItem("Booking #" + b.getId() + " - Rs." + b.getTotalPrice());
        }
    }

    private void loadPayments() {
        DefaultTableModel model = new DefaultTableModel();
        model.addColumn("ID");
        model.addColumn("Booking ID");
        model.addColumn("Amount");
        model.addColumn("Date");
        model.addColumn("Method");
        model.addColumn("Status");

        List<Payment> list = paymentDAO.getAllPayments();
        for (Payment p : list) {
            model.addRow(new Object[]{
                p.getId(), p.getBookingId(), p.getAmount(),
                p.getPaymentDate(), p.getMethod(), p.getStatus()
            });
        }
        jTable1.setModel(model);
    }

    private void clearFields() {
        jTextField1.setText("");
        selectedId = -1;
    }

